// operadores realacionais
int n1 = 1;
int n2 = 2;

var igualdade = n1 == n2;

var menor = n1 < n2;

var maior = n2 > n1;


 var menorigual = n1 <= n2;

var maiorigual = n1 >= n2;

var diferente = n1 != n2;

Console.WriteLine("Igualdade " + igualdade);
Console.WriteLine("Maior " + maior);
Console.WriteLine("Menor " + menor);
Console.WriteLine("Maior ou igual " + maiorigual);
Console.WriteLine("Menor ou igual" + menorigual);
Console.WriteLine("Diferente" + diferente);






